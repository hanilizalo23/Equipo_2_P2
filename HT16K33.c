static uint8_t led_matrix = NOTHING;
static uint8_t InitByte1 = (0x21U);
static uint8_t InitByte2 = (0x81U);
static uint8_t InitByte3 = (0xEFU);

uint16_t Led_Matrix_config(uint8_t slave_address_matrix)
{
	uint16_t status = NOTHING;
	led_matrix = slave_address_matrix;
	status = I2C_write(led_matrix, InitByte1, NOTHING, &InitByte1, SUB_ADDR_SIZE);
	status = I2C_write(led_matrix, InitByte2, NOTHING, &InitByte2, SUB_ADDR_SIZE);
	status = I2C_write(led_matrix, InitByte3, NOTHING, &InitByte3, SUB_ADDR_SIZE);
	return status;
}
uint16_t Led_Matrix_Write(uint8_t *arr_ptr)
{
	return I2C_write(led_matrix, *arr_ptr, LENGHT, LED_MATRIX_ADDR, SUB_ADDR_SIZE);
}

